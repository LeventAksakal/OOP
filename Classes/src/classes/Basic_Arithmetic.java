package classes;
public class Basic_Arithmetic 
{
    public int add(int x, int y)
    {
       return x+y; 
    }
    public int subtrack(int x, int y)
    {
       return x-y; 
    }
    public int multiply(int x, int y)
    {
       return x*y; 
    }
    public int divide(int x, int y)
    {
       return x/y; 
    }
}
